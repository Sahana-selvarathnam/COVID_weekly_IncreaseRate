    "import pandas as pd\n",
    "import csv\n",
    "import requests\n",
    "import numpy as np\n",
    "import json\n",
    "import plotly.express as px\n",
    "import plotly.io as pio\n",
    "\n",
    " \n",
    "#Download_csv_file\n",
    "\n",
    "CSV_URL = \"https://api.covid19india.org/csv/latest/states.csv\"\n",
    "with requests.Session() as s:\n",
    "    download = s.get(CSV_URL)\n",
    "\n",
    "\n",
    "#Read and write to csv\n",
    "\n",
    "with open('covid19india_states.csv', 'w') as f:\n",
    "    writer = csv.writer(f)\n",
    "    for line in download.iter_lines():\n",
    "        writer.writerow(line.decode('utf-8').split(','))\n",
    "\n",
    "\n",
    "#Creating Pivot table\n",
    "\n",
    "districts=pd.read_csv('covid19india_states.csv')\n",
    "table = pd.pivot_table(districts, values='Confirmed', index=['State'], columns=['Date'], fill_value=0)\n",
    "table.to_csv(\"pivot.tsv\", sep='\\t')\n",
    "pivot_table=pd.read_csv(\"pivot.tsv\", sep=\"\\t\")\n",
    "\n",
    "#Creating two weeks data\n",
    "\n",
    "two_weeks = pivot_table.T.tail(14).T\n",
    "first_week = two_weeks.T.head(7).T\n",
    "last_week = two_weeks.T.tail(7).T\n",
    "first_column = pivot_table.T.head(2).T\n",
    "\n",
    "\n",
    "first_week['avg_cases_over_7_days'] = first_week.mean(axis=1)\n",
    "last_week['avg_cases_previous_week'] = last_week.mean(axis=1)\n",
    "week1 = first_column.join(first_week)\n",
    "week2 = first_column.join(last_week)\n",
    "\n",
    "\n",
    "merged = week1.merge(week2, on=['State'])\n",
    "merged['Increase'] = merged['avg_cases_over_7_days']-merged['avg_cases_previous_week']\n",
    "merged['Rate_of_Increase']=(merged['Increase']/merged['avg_cases_previous_week'])*100\n",
    "\n",
    "\n",
    "searchfor=['India','State Unassigned']\n",
    "merged2 = merged[~merged.State.str.contains('|'.join(searchfor))]\n",
    "\n",
    "\n",
    "header=['State','avg_cases_over_7_days','avg_cases_previous_week','Increase','Rate_of_Increase']\n",
    "merged2.to_csv(\"state.tsv\", sep='\\t',columns=header)\n",
    "\n",
    "\n",
    "#Creating image\n",
    "pio.renderers.default = 'browser'\n",
    "\n",
    "india_states = json.load(open(\"india_states.geojson\", \"r\"))\n",
    "\n",
    "state_id_map = {}\n",
    "for feature in india_states[\"features\"]:\n",
    "    feature[\"id\"] = feature[\"properties\"][\"st_code\"]\n",
    "    state_id_map[feature[\"properties\"][\"id\"]] = feature[\"id\"]\n",
    "    \n",
    "\n",
    "df = pd.read_csv(\"state.tsv\",sep='\\t')\n",
    "df[\"id\"] = df[\"State\"].apply(lambda x: state_id_map[x])\n",
    "df.loc[df['Rate_of_Increase'] <= 0, 'Increase_image'] = 0\n",
    "df.loc[df['Rate_of_Increase'] > 0, 'Increase_image'] = df['Rate_of_Increase']\n",
    "\n",
    "fig = px.choropleth(\n",
    "    df,\n",
    "    locations=\"id\",\n",
    "    geojson=india_states,\n",
    "    color=\"Rate_of_Increase\",\n",
    "    hover_name=\"State\",\n",
    "    hover_data=[\"Rate_of_Increase\"],\n",
    "    title=\"Rate of Increase in COVID19 cases per week\",\n",
    "    color_continuous_scale=px.colors.diverging.BrBG,\n",
    "    color_continuous_midpoint=0.1)\n",
    "fig.update_geos(fitbounds=\"locations\", visible=False)\n",
    "fig.show()"
